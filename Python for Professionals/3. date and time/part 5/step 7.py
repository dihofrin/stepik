from datetime import datetime, timedelta

dates = {}
pattern = '%d.%m.%Y'
dt = datetime.strptime(input(), pattern)
next_days = []
for i in range(7):
    dt += timedelta(days=1)
    next_days.append(dt)

for _ in range(int(input())):
    *names, birthday = input().split()
    birthday = datetime.strptime(birthday, pattern)
    dates[birthday] = names

close = []

for i in dates.keys():
    z = datetime(dt.year, i.month, i.day)
    if z in next_days:
        close.append(i)

if len(close) == 0:
    print('–î–Ω–∏ —Ä–æ–∂–¥–µ–Ω–∏—è –Ω–µ –ø–ª–∞–Ω–∏—Ä—É—é—Ç—Å—è')
else:
    print(*dates[max(close)])
