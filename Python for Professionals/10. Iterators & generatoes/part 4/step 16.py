LANGUAGES = {'en': 'abcdefghijklmnopqrstuvwxyz', 'ru': '–∞–±–≤–≥–¥–µ–∂–∑–∏–π–∫–ª–º–Ω–æ–ø—Ä—Å—Ç—É—Ñ—Ö—Ü—á—à—â—ä—ã—å—ç—é—è'}

class Alphabet:

    def __init__(self, language):
        self.language = list(LANGUAGES[language])
        self.index = -1

    def __iter__(self):
        return self

    def __next__(self):
        self.index += 1
        if self.index == len(self.language) - 1:
            self.index = -1
        return self.language[self.index]

