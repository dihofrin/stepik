from re import findall, search
from sys import stdin


tags = {}
for line in stdin:
    for item in findall(r'<([^\/].*?)>', line):
        tag = search(r'(\b[\w]+\b)', item).group(1)
        attr = sorted(findall(r'([\w+-]+)=', item) or tuple())
        tags[tag] = tags.get(tag, set()) | set(attr)

[print(f'{tag}:', ', '.join(sorted(tags[tag]))) for tag in sorted(tags)]