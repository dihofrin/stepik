class LoopTracker:

    def __init__(self, iterable):
        self.iterable = iterable
        self.count = -1
        self._first = self.iterable[0]
        self._last = self.iterable[-1]
        self._empty_accesses = 0

    @property
    def accesses(self):
        return self.count + 1

    @property
    def empty_accesses(self):
        return self._empty_accesses

    @property
    def first(self):
        if not self.iterable:
            raise AttributeError('–ò—Å—Ö–æ–¥–Ω—ã–π –∏—Ç–µ—Ä–∏—Ä—É–µ–º—ã–π –æ–±—ä–µ–∫—Ç –ø—É—Å—Ç')
        return self._first

    @property
    def last(self):
        return self._last

    def is_empty(self):
        return self.count < len(self.iterable)

    def __iter__(self):
        return self

    def __next__(self):
        self.count += 1
        if self.count >= len(self.iterable):
            self._empty_accesses += 1
            raise StopIteration
        if self.count == 0:
            self._first = self.iterable[self.count]
        self._last = self.iterable[self.count]
        return self.iterable[self.count]


