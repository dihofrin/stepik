class Grouper:

    def __init__(self, iterable, key):
        self.group = dict()
        self.iterable = iterable
        for i in self.iterable:
            self.group.setdefault(key(i), []).append(i)

    def add(self, obj):
        self.group.setdefault(self.key(obj), []).append(obj)

    def group_for(self, obj):
        return self.key(obj)

    def __len__(self):
        return len(self.group)

    def __iter__(self):
        yield from self.group.items()

    def __repr__(self):
        return f'{self.group}'

    def __contains__(self, item):
        return item in self.group

    def __getitem__(self, item):
        return self.group[item]

grouper = Grouper(['bee', 'geek', 'one', 'two', 'five', 'hi'], key=len)

