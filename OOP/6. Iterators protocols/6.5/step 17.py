class HtmlTag:

    level = -1

    def __init__(self, tag, inline=False):
        self.open_tag = f'<{tag}>'
        self.closed_tag = f'</{tag}>'
        self.wp = '' if inline else '\n'
        self.sp = '  '
        self.inline = inline

    def __enter__(self):
        HtmlTag.level += 1
        print(self.sp * HtmlTag.level + self.open_tag, end=self.wp)
        return self

    def __exit__(self, exc_type, exc_val, exc_tb):
        if self.inline:
            print(self.closed_tag)
        else:
            print(self.sp * HtmlTag.level + self.closed_tag)
        HtmlTag.level -= 1
    def print(self, text):
        if self.inline:
            print(text, end='')
        else:
            print(HtmlTag.level * '    ' + text)
