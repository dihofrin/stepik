def recviz(func):
    tabs = -1
    def wrapper(*args, **kwargs):
        nonlocal tabs
        tabs += 1
        a = [repr(i) for i in args]
        kwa = [f'{k}={repr(v)}' for k, v in kwargs.items()]
        print(f'{"    "*tabs}-> {func.__name__}({", ".join((a + kwa))})')
        f = func(*args, **kwargs)
        print(f'{"    "*tabs}<- {repr(f)}')
        tabs -= 1
        return f
    return wrapper
